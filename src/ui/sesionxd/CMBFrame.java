    private void cmbdeptItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbdeptItemStateChanged
        int index=cmbdept.getSelectedIndex();
        if(index==0){
            munmod=new DefaultComboBoxModel(new String[0]);
            cmbmunip.setModel(munmod);
            return;
        }
        index--;
        try {
            secuencial=new Secuencial("munip.csv");
            String[] multimunip=secuencial.readText().split("\n");
            String[] m=multimunip[index].split(",");
            munmod=new DefaultComboBoxModel(m);
            cmbmunip.setModel(munmod);
        } catch (IOException ex) {
            Logger.getLogger(CMBFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cmbdeptItemStateChanged

