    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            secuencial= new Secuencial("dept.csv");
            String dep=secuencial.readText();
            String[] depts=dep.split(",");
            String[] cop=new String[depts.length+1];
            System.arraycopy(depts, 0, cop,1, depts.length);
            cop[0]="Seleccione una opcion";
            munmod=new DefaultComboBoxModel(new String[0]);
            defmod=new DefaultComboBoxModel(cop);
            cmbdept.setModel(defmod);
            cmbmunip.setModel(munmod);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(CMBFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(CMBFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_formWindowOpened

